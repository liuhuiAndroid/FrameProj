public class RealNameEntity {
    private String realName;
    private String identityNo;
    private String frontIdentityPath;
    private String afterIdentityPath;

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getIdentityNo() {
        return identityNo;
    }

    public void setIdentityNo(String identityNo) {
        this.identityNo = identityNo;
    }

    public String getFrontIdentityPath() {
        return frontIdentityPath;
    }

    public void setFrontIdentityPath(String frontIdentityPath) {
        this.frontIdentityPath = frontIdentityPath;
    }

    public String getAfterIdentityPath() {
        return afterIdentityPath;
    }

    public void setAfterIdentityPath(String afterIdentityPath) {
        this.afterIdentityPath = afterIdentityPath;
    }
}
